    def ask_setting():
        return input("Where did the dream take place? ")
    def ask_characters():
        return input("Who were the characters in the dream? Was there anyone that you liked or disliked?")

    def ask_emotions():
        return input("What emotions did you experience in the dream or when you woke up? ")

    def ask_actions():
        return input("What were you doing in the dream?")
    
    def ask_symbols():
        return input("Where there and objects or symbols that stood out to you?")
    
    def ask_experiences():
        return input("Was the dream familiar or related to any real life experiences?")
    
    